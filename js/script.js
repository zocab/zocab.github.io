function randomNumber(min, max)  {
  return Math.floor(Math.random() * (max - min) ) + min;
}

window.addEventListener('load', function() {
  const enExampleTarget = document.getElementById('enExample');
  const viExampleTarget = document.getElementById('viExample');
  const wordTarget = document.getElementById('word');
  const pronunciation = document.getElementById('pronunciation');
  
  const index = randomNumber(0, DATA.length);
  
  wordTarget.innerText = DATA[index].word;
  pronunciation.innerText = DATA[index].pronounce;
  viExampleTarget.innerText = DATA[index].ex_vi;
  enExampleTarget.innerText = DATA[index].ex_en;
  
  viExampleTarget.addEventListener('click', function() {
    viExampleTarget.classList.toggle('blur');
  })


  let isEn = true;
  wordTarget.addEventListener('click', function() {
    if (isEn) {
      wordTarget.innerHTML = DATA[index].meaning;
    } else {
      wordTarget.innerHTML = DATA[index].word;
    }
    isEn = !isEn;
  })
})
